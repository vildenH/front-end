<?php

    // 前置++  后置++    前置--  后置--
    $a = 10;
    $b = 5;

    $c = $a++ + $b--;   //a:11   b:4    c:15
    $d = ++$a - --$c;   //a:12    c:14    d:-2
    $e = $c++ + ++$b;   //b:5   c:15   e:19
    $f = ++$e - --$a;   //a:11    e :20   f:9

    //求$a, $b ，$c ,$d ,$e ,$f的值
    echo $a.'<br>';  //11
    echo $b.'<br>';  //5
    echo $c.'<br>';  //15
    echo $d.'<br>';  //-2
    echo $e.'<br>';  //20
    echo $f.'<br>';  //9